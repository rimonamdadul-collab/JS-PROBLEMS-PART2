function WoodQuantity(shirtQuantity,pantQuantity,shopeQuantity){
    const shirtWood = 500;
    const pantWood = 300;
    const shopeWood = 1300;
const shirt = shirtQuantity * shirtWood
const pant = pantQuantity * pantWood
const shope = shopeQuantity * shopeWood
const total = shirt + pant+ shope

return total
}

